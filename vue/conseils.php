<ul> 
	<li class="title_h2"> Conseils pour reduire votre facture d electricite</li>
		<ul class="text_conseils"> 
			<li> Choisissez des appareils a basse consommation. Les appareils de categorie A+++ sont plus chers, mais consomment jusqu’à 10 fois moins. </li>
			<li> Remplacez vos ampoules traditionnelles par des ampoules à economie d energie (ampoules basse consommation, ampoules LED) qui durent trois fois plus et consomment trois fois moins. Pensez également à profiter au maximum de la lumière du jour… gratuite. </li>
			<li> Degivrez régulièrement votre refrigerateur avant que la couche de givre n atteigne 3 mm d’épaisseur, ce qui vous permettra d economiser jusqu a 30 % de sa consommation d electricite. </li>
			<li> Faites fonctionner de preference vos electromenagers pendant les heures creuses, de 10 h 30 le soir à 6 h 30 le matin en general (les horaires varient en fonction des régions !), car l electricite y est environ 40 % moins chere. </li>
		</ul>
	<li class="title_h2"> Conseils pour reduire sa consommation d eau </li>
		<ul> 
			<li> Prendre un bain de temps en temps apporte un reel plaisir. Mais ce plaisir a un cout des lors qu’il devient quotidien. Lorsqu une douche de quelques minutes réclame 40 à 60 litres, un bain en demande, lui, entre 150 et 200. </li>
			<li> 30 : c’est en litres l economie realisee par vaisselle entre une plonge à la main et un lavage en machine (correspondant a trois repas pris par une famille de quatre personnes).</li>
			<li> Arroser efficacement son jardin </li>
			<li> Ne pas laisser couler l eau pendant le rasage, le lavage des mains ou des dents. Derriere ce simple geste vous reduirez votre consommation d eau de quelques precieux litres.</li>
		</ul>
